const express = require('express');
const { middleware, messagingApi } = require('@line/bot-sdk');
const { GoogleGenAI } = require('@google/genai');

const app = express();

// 1. โหลดและตรวจสอบ Config จาก Environment Variables
const config = {
  channelAccessToken: process.env.LINE_TOKEN,
  channelSecret: process.env.LINE_SECRET,
};

const GEMINI_API_KEY = process.env.GEMINI_API_KEY;

if (!config.channelAccessToken || !config.channelSecret || !GEMINI_API_KEY) {
  console.error('Error: กรุณาตั้งค่า LINE_TOKEN, LINE_SECRET และ GEMINI_API_KEY ใน Environment Variables ให้ครบถ้วน');
  process.exit(1);
}

// 2. Initial LINE Client และ Gemini SDK
const client = new messagingApi.MessagingApiClient({
  channelAccessToken: config.channelAccessToken
});
const ai = new GoogleGenAI({ apiKey: GEMINI_API_KEY });

// 3. Object สำหรับเก็บสถานะโหมดแชทของผู้ใช้ (แยกตาม userId ของ LINE)
const userStates = {};

// 4. รายการคำสั่งที่ต้องหยุดทำงานทันที (CRITICAL SYSTEM FILTER)
const filterKeywords = [
  "โรคทางเพศสัมพันธ์", 
  "การป้องกันโรค", 
  "รู้ทันท้อง", 
  "ยาเสพติด", 
  "คำถามว้าวุ่น"
];

// 5. System Prompt หลักสำหรับ "พี่หมี AI" ในโหมดเปิดการสนทนา
const SYSTEM_PROMPT = `คุณคือ "พี่หมี AI" เจ้าหน้าที่สาธารณสุขสุดใจดี อ่อนโยน และเข้าใจวัยรุ่น ประจำ LINE OA โครงการวัยรุ่นวัยใส ของโรงพยาบาลส่งเสริมสุขภาพตำบล (รพ.สต.)

สไตล์การพูดคุย (Tone of Voice):
- ใช้ภาษาเป็นกันเอง อ่อนโยน ปลอบโยน ไม่ตัดสิน ไม่ดุดัน ไม่สั่งสอนแบบผู้ใหญ่
- แทนตัวเองว่า "พี่หมี" และแทนผู้ใช้วัยรุ่นว่า "เธอ" หรือ "น้อง"
- ใช้温馨อีโมจิบ้างตามความเหมาะสม (เช่น 🧸, 💖, 🍃, 🔋)
- ย้ำเสมอว่า "ทุกอย่างที่คุยกับพี่หมีเป็นความลับนะ ไม่ต้องกังวลเลย"

ขอบเขตความรู้และหน้าที่ (Scope of Work):
1. ให้คำปรึกษาและข้อมูลที่ถูกต้องเกี่ยวกับ โรคติดต่อทางเพศสัมพันธ์และการป้องกัน (ถุงยางอนามัย), ภัยของสารเสพติด พร้อมวิธีปฏิเสธเพื่อน, ปัญหาสุขภาพจิต (ความเครียด, ดิ่ง, ซึมเศร้า) โดยเน้นการรับฟังและฮีลใจ
2. ข้อมูลบริการของ รพ.สต. ในพื้นที่: แจกถุงยางอนามัยและยาเม็ดคุมกำเนิด "ฟรี" สำหรับวัยรุ่น เดินเข้ามาขอรับได้เลยที่ รพ.สต. ในวันจันทร์ - ศุกร์ เวลา 08.30 - 16.30 น. (เว้นวันหยุดราชการ)

ข้อจำกัดสำคัญ (Guardrails):
- ห้ามวินิจฉัยโรคเองเด็ดขาด ห้ามจ่ายยาหรือสั่งยา
- หากน้องพิมพ์คำว่า "อยากตาย" "ทำร้ายตัวเอง" ให้แสดงความห่วงใยอย่างที่สุด และส่งข้อความนี้ปิดท้ายทันที: "พี่หมีอยู่ตรงนี้นะ แต่อยากให้อุ่นใจขึ้น ลองโทรคุยกับสายด่วนสุขภาพจิต 1323 (โทรฟรี 24 ชม.) หรือกดปุ่ม 'ปรึกษาเจ้าหน้าที่' เพื่อคุยกับพี่ ๆ อนามัยคนจริง ๆ ได้เลยนะ ทุกอย่างเป็นความลับแน่นอนครับ 🧸"`;

// 6. ฟังก์ชันหลักสำหรับจัดการ Events จาก LINE
async function handleEvent(event) {
  // สนใจเฉพาะข้อความที่เป็น Text และมีข้อมูลผู้ใช้ (userId) เท่านั้น
  if (event.type !== 'message' || event.message.type !== 'text' || !event.source.userId) {
    return null;
  }

  const userId = event.source.userId;
  const userText = event.message.text.trim();

  // ปรับสถานะเริ่มต้นหากผู้ใช้คนนี้ยังไม่เคยอยู่ในระบบ
  if (!userStates[userId]) {
    userStates[userId] = 'OFF';
  }

  // --- CRITICAL SYSTEM FILTER ---
  // ถ้าเจอคำในหมวดหมู่ที่กำหนด ให้หยุดการทำงาน (Return เพื่อไปตอบ 200 OK)
  if (filterKeywords.some(keyword => userText.includes(keyword))) {
    console.log(`[Filter] ผู้ใช้ ${userId} พิมพ์ "${userText}": หยุดทำงานเพื่อให้ Rich Menu จัดการ`);
    return null;
  }

  // --- โหมดสุ่มคำคมฮีลใจ (ปรับปรุงใหม่: ดึงจากคลังข้อความหลังบ้านโดยตรง ไม่ใช้ AI เพื่อความเร็วและความเสถียร) ---
  if (userText === "คำคมฮีลใจ") {
    try {
      // คลังคำคมฮีลใจสไตล์พี่หมี (สามารถเพิ่มหรือแก้ไขประโยคเพิ่มเติมใน Array นี้ได้ตามต้องการ)
      const bearQuotes = [
        "วันนี้อาจจะหนักหน่อย แต่เธอเก่งมากแล้วนะที่พาตัวเองผ่านมันมาได้จนถึงตอนนี้... พักผ่อนให้เต็มที่นะคนเก่ง 🤍💤",
        "อย่าลืมใจดีกับตัวเองเยอะๆ นะครับ วันนี้ทำดีที่สุดแล้ว ทิ้งเรื่องเครียดๆ ไว้ข้างหลัง แล้วกลับมาดูแลหัวใจตัวเองกันนะ 🍃✨",
        "ไม่จำเป็นต้องเก่งตลอดเวลาก็ได้ วันไหนเหนื่อยก็แค่ยอมรับว่าเหนื่อย แล้วให้โอกาสตัวเองได้หยุดพักบ้างนะ",
        "เก่งมากแล้วนะที่เติบโตมาจนถึงวันนี้ ผ่านอะไรมาตั้งมากมาย ขอบคุณตัวเองหน่อยนะคนเก่ง 🌟💖",
        "ท้องฟ้าแต่ละวันยังสีไม่เหมือนกันเลย วันนี้ไม่สดใสก็ไม่เป็นไรนะ พรุ่งนี้ค่อยเริ่มใหม่ ส่งพลังใจให้เต็มร้อยเลยครับ! 🍃🔋",
        "ความทุกข์ของวันนี้ก็เหมือนสายฝน ตกหนักแค่ไหน สุดท้ายมันก็ต้องซาลง แล้วท้องฟ้าหลังฝนจะกลับมาสดใสแน่นอน ☀️🌈",
        "ถ้าชาร์จแบตให้โทรศัพท์ทุกวัน ก็อย่าลืมชาร์จแบตให้หัวใจตัวเองด้วยนะ วันนี้เหนื่อยมาพอแล้ว ปิดโหมดรับเรื่องเครียดแล้วพักผ่อนเถอะ 🔋ชิวๆ",
        "วันแย่ๆ มันก็แค่วันหนึ่งวัน ไม่ใช่ทั้งชีวิตสักหน่อย ทิ้งความกังวลไว้ในวันนี้ แล้วตื่นมาเจอวันใหม่ที่สดใสกว่าเดิมกันนะ",
        "โลกข้างนอกจะใจร้ายหรือวุ่นวายแค่ไหนไม่สำคัญ ขอให้พื้นที่ตรงนี้เป็นเซฟโซนที่ปลอดภัย คอยโอบกอดความเหนื่อยล้าของเธอเสมอนะ 🏡✨",
        "ไม่ต้องรีบวิ่งตามใครหรอก เดินในจังหวะที่ตัวเองสบายใจ เหนื่อยก็นั่งพัก หายเหนื่อยค่อยเดินต่อ ชีวิตเป็นของเรานะ 🚶‍♂️ร่มรื่น",
        "ไม่ว่าเจออะไรมาจนรู้สึกเหนื่อยล้า หันมาเมื่อไหร่ก็มีมุมสบายใจตรงนี้รออยู่เสมอ แวะมาเติมพลังใจได้ตลอดเลยนะฮะ 🤍🔋",
        "ทิ้งเรื่องหนักๆ ที่แบกไว้ลงก่อนนะ ตอนนี้ไม่มีอะไรต้องกังวลแล้ว ปล่อยให้จิตใจได้พักผ่อนอย่างเต็มที่เลยครับ",
        "วันนี้อาจจะมีเรื่องให้เหนื่อยจนล้าไปหมด แต่เธอเก่งมากแล้วนะที่ประคับประคองตัวเองจนผ่านมันมาได้ พักผ่อนได้แล้วนะคนเก่ง 🤍",
        "ไม่ว่าผลลัพธ์ของวันนี้จะเป็นยังไง ความตั้งใจและความพยายามของเธอในวันนี้มันน่าภูมิใจที่สุดแล้วล่ะ ✨",
        "ขอบคุณนะที่อดทนและไม่ยอมแพ้มาจนถึงตอนนี้ วันนี้เก่งมากจริงๆ ทิ้งเรื่องหนักๆ แล้วไปนอนหลับฝันดีนะ",
        "แค่วันนี้เธอยังยิ้มได้ หรือผ่านมันมาได้โดยไม่แตกสลาย ก็นับว่าเธอเก่งและสุดยอดมากๆ แล้วครับ 🌟",
        "ความเก่งของแต่ละวันไม่เท่ากันหรอก วันไหนที่ทำได้แค่นอนพักผ่อนเฉยๆ ก็นับว่าเก่งมากแล้วที่ยอมใจดีกับตัวเอง 🍃",
        "เรื่องของวันนี้ ให้มันจบลงแค่วันนี้นะ ทิ้งความกังวลทับถมไว้ข้างหลัง แล้วเปิดโหมดพักผ่อนให้หัวใจได้ชาร์จแบตเต็มที่กันเถอะ 🔋✨",
        "ไอ้ต้าวความสุขอาจจะแวะไปทำงานที่อื่นแปบนึง ไม่เป็นไรนะ... วันนี้เอาพลังใจตรงนี้ไปเติมให้เต็มร้อยก่อน พรุ่งนี้ค่อยว่ากันใหม่! 🔋💖",
        "ถ้าเครื่องยนต์ยังต้องมีวันดับเครื่องพัก หัวใจเธอก็ต้องการเวลาปิดโหมดเรื่องเครียดเหมือนกันนะ คืนนี้ปล่อยใจให้โล่งแล้วพักผ่อนเถอะ",
        "วันแย่ๆ ก็แค่ 24 ชั่วโมง พอกลางคืนผ่านไป มันก็กลายเป็นอดีตแล้ว ตื่นมาเริ่มรันความสดใสกันใหม่ในวันพรุ่งนี้นะฮะ ☀️",
        "เหนื่อยล้าจากตรงไหนมา วางมันลงก่อนนะ ตอนนี้ไม่มีอะไรต้องแบกรับแล้ว ได้เวลาคืนความสงบและรอยยิ้มให้ตัวเองแล้วครับ"
      ];

      // สุ่มเลือกตำแหน่งข้อความใน Array
      const randomIndex = Math.floor(Math.random() * bearQuotes.length);
      const randomQuote = bearQuotes[randomIndex];
      
      return await client.replyMessage({
        replyToken: event.replyToken,
        messages: [{ type: 'text', text: randomQuote }]
      });
    } catch (error) {
      console.error('Error ในการส่งคำคมฮีลใจ:', error);
      return await client.replyMessage({
        replyToken: event.replyToken,
        messages: [{ type: 'text', text: 'พี่หมีขอเวลาคิดคำคมดีๆ แป๊บน้าา ตอนนี้สมองตื้อไปหมดแล้ว 🧸' }]
      });
    }
  }

  // --- เงื่อนไขสลับโหมดเปิดการสนทนา [CHAT_MODE = ON] ---
  if (userText === "คุยกับพี่หมี AI") {
    userStates[userId] = 'ON';
    console.log(`[State] ผู้ใช้ ${userId} เปลี่ยนสถานะเป็น ON`);
    return await client.replyMessage({
      replyToken: event.replyToken,
      messages: [{
        type: 'text',
        text: 'พี่หมีสแตนด์บายแล้วครับ! มีอะไรอยากระบายหรือเล่าให้พี่หมีฟัง พิมพ์มาได้เลยนะ พี่หมีพร้อมรับฟังเสมอ ทุกเรื่องที่คุยกันเป็นความลับแน่นอนครับ 🧸'
      }]
    });
  }

  // --- เงื่อนไขสลับโหมดปิดการสนทนา [CHAT_MODE = OFF] ---
  if (userText === "บ๊ายบายพี่หมี") {
    userStates[userId] = 'OFF';
    console.log(`[State] ผู้ใช้ ${userId} เปลี่ยนสถานะเป็น OFF`);
    return await client.replyMessage({
      replyToken: event.replyToken,
      messages: [{
        type: 'text',
        text: 'รับทราบครับ! ไว้เหนื่อยเมื่อไหร่แวะมาคุยกันใหม่นะ พี่หมีส่งต่อให้พี่ๆ เจ้าหน้าที่ดูแลต่อแล้วครับ บ๊ายบายครับ 🔋'
      }]
    });
  }

// --- กรณีที่อยู่ในโหมดสนทนา [CHAT_MODE === ON] และไม่ใช่คำสั่งเปิด/ปิด/คำคม ---
if (userStates[userId] === 'ON') {
  try {
    // ปรับโครงสร้าง parameter ให้ตรงตามข้อกำหนดของ @google/genai ล่าสุด
    const response = await ai.models.generateContent({
      model: 'gemini-1.5-flash',
      contents: userText,
      config: {
        systemInstruction: SYSTEM_PROMPT  // อยู่ข้างใน config ออบเจกต์ตัวเดียวตรงๆ
      }
    });

    // ดึงค่าข้อความที่ตอบกลับ (ใน SDK ใหม่แนะนำให้ตรวจสอบ .text)
    const aiReply = response.text ? response.text.trim() : "พี่หมีกำลังฟังอยู่นะครับ เล่าต่อได้เลยนะ 🧸";

    // รีเซ็ตตัวนับคะแนนความผิดพลาดเมื่อทำงานสำเร็จ
    userStates[userId + '_error_count'] = 0;

    return await client.replyMessage({
      replyToken: event.replyToken,
      messages: [{ type: 'text', text: aiReply }]
    });

  } catch (error) {
    console.error('Gemini API Error (แชท):', error);

    // ตัวนับรอบเออเรอร์ทำงาน
    if (!userStates[userId + '_error_count']) {
      userStates[userId + '_error_count'] = 1;
    } else {
      userStates[userId + '_error_count'] += 1;
    }

    let errorMessage = 'ขออภัยครับ พี่หมีกำลังมึนงงเล็กน้อย พิมพ์มาใหม้อีกครั้งนะ 🧸';

    if (userStates[userId + '_error_count'] >= 2) {
      errorMessage = 'พี่หมีว่าลองเปลี่ยนเป็นคุยกับเจ้าหน้าที่โดยตรงเลยดีไหมครับ พี่ๆเจ้าหน้าที่พร้อมให้คำปรึกษาเสมอน้าา แล้วทุกอย่างจะเป็นความลับแน่นอน';
      userStates[userId + '_error_count'] = 0; 
    }

    return await client.replyMessage({
      replyToken: event.replyToken,
      messages: [{ type: 'text', text: errorMessage }]
    });
  }
}

  // --- คำสั่งอื่นๆ ขณะที่สถานะเป็น OFF (หยุดทำงานเพื่อให้ระบบหลักทำงาน) ---
  console.log(`[Filter] ผู้ใช้ ${userId} ส่งข้อความ "${userText}" นอกเงื่อนไขแชท: หยุดทำงานเพื่อให้ระบบหลักทำงาน`);
  return null;
}

// 7. Routing สำหรับ Webhook
app.post('/webhook', middleware(config), (req, res) => {
  Promise
    .all(req.body.events.map(handleEvent))
    .then(() => res.status(200).send('OK'))
    .catch((err) => {
      console.error('Webhook Error:', err);
      res.status(500).end();
    });
});

// 8. Start Server
const PORT = process.env.PORT || 3000;
app.listen(PORT, () => {
  console.log(`พี่หมี AI Webhook กำลังรันที่พอร์ต ${PORT}`);
});
